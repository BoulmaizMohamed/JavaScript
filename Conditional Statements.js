// if statement
let age = 20;
if (age >= 18) {
  console.log("You are an adult.");
}

// if-else statement
let temperature = 25;
if (temperature > 30) {
  console.log("It's too hot!");
} else {
  console.log("It's comfortable.");
}

// switch statement
let day = "Monday";
switch (day) {
  case "Monday":
    console.log("Today is Monday.");
    break;
  case "Tuesday":
    console.log("Today is Tuesday.");
    break;
  default:
    console.log("Today is not Monday or Tuesday.");
}
